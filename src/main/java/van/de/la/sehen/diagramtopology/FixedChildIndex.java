package van.de.la.sehen.diagramtopology;

import java.util.Objects;

public class FixedChildIndex implements ChildIndex {
    private double x;
    private double y;

    public FixedChildIndex(double x, double y) {
        this.x = x;
        this.y = y;
    }

    public double getX() {
        return x;
    }

    public double getY() {
        return y;
    }

    @Override
    public boolean equals(Object o) {
        if (this == o) return true;
        if (o == null || getClass() != o.getClass()) return false;
        FixedChildIndex that = (FixedChildIndex) o;
        return Double.compare(that.x, x) == 0 &&
                Double.compare(that.y, y) == 0;
    }

    @Override
    public int hashCode() {
        return Objects.hash(x, y);
    }
}
